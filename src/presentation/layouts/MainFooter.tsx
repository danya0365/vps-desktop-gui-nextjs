/**
 * MainFooter
 * MacOS-style dock with working navigation
 */
export function MainFooter() {
  const pathname = usePathname();

  return (
    <footer className="fixed bottom-4 left-1/2 -translate-x-1/2 z-50">
      <nav className="macos-dock">
        {dockItems.map((item) => (
          <DockIcon
            key={item.id}
            item={item}
            isActive={
              item.href === "/" ? pathname === "/" : pathname.startsWith(item.href)
            }
          />
        ))}
      </nav>
    </footer>
  );
}

interface DockIconProps {
  item: DockItem;
  isActive: boolean;
}

function DockIcon({ item, isActive }: DockIconProps) {
  const [hovered, setHovered] = useState(false);

  const spring = useSpring({
    transform: hovered ? "translateY(-8px) scale(1.2)" : "translateY(0px) scale(1)",
    config: { tension: 400, friction: 20 },
  });

  const tooltipSpring = useSpring({
    opacity: hovered ? 1 : 0,
    transform: hovered ? "translateY(-8px)" : "translateY(0px)",
    config: { tension: 300, friction: 20 },
  });

  return (
    <div className="relative">
      {/* Tooltip */}
      <animated.div
        style={tooltipSpring}
        className="absolute -top-10 left-1/2 -translate-x-1/2 px-3 py-1.5 bg-gray-900 dark:bg-gray-700 text-white text-xs font-medium rounded-lg whitespace-nowrap pointer-events-none"
      >
        {item.label}
        <div className="absolute top-full left-1/2 -translate-x-1/2 border-4 border-transparent border-t-gray-900 dark:border-t-gray-700" />
      </animated.div>

      {/* Icon */}
      <Link href={item.href}>
        <animated.div
          style={spring}
          className={`dock-item ${isActive ? "dock-item-active" : ""} ${
            isActive
              ? "bg-primary/20 text-primary dark:bg-primary/30"
              : "bg-white/80 dark:bg-gray-800/80 text-gray-700 dark:text-gray-300"
          } hover:bg-white dark:hover:bg-gray-700`}
          onMouseEnter={() => setHovered(true)}
          onMouseLeave={() => setHovered(false)}
          aria-label={item.label}
        >
          {item.icon}
        </animated.div>
      </Link>
    </div>
  );
}
